	private static void updateFacturasAttribute(int id_Id, String valor, String campo) throws Exception
	{
		Facturas obj = BaseFacturasManager.getFacturas(id_Id);
			if(campo.equals(FacturasPeer.ID))
					obj.setId(Integer.parseInt(valor)); //int
					if(campo.equals(FacturasPeer.FECHA))
					obj.setFechaIni(valor); //String
					if(campo.equals(FacturasPeer.IMPORTE))
					obj.setImporte(Double.parseDouble(valor)); //double
					if(campo.equals(FacturasPeer.ESTADO))
					obj.setEstado(Integer.parseInt(valor)); //int
					if(campo.equals(FacturasPeer.DESCRIPCION))
					obj.setDescripcion(valor); //String
					if(campo.equals(FacturasPeer.OBSERVACION))
					obj.setObs(valor); //String
					if(campo.equals(FacturasPeer.CLIENTE_ID))
					obj.setClienteId(Integer.parseInt(valor)); //int
					if(campo.equals(FacturasPeer.REPARACION_ID))
					obj.setReparacionId(Integer.parseInt(valor)); //int
					obj.save();
	}

